export const THEMES: ProwlTheme[] = [midnight, bloodmoon, ghost, phosphor, arctic, venom];

export function getTheme(id: string): ProwlTheme {
  return THEMES.find(t => t.id === id) ?? midnight;
}

export function applyTheme(theme: ProwlTheme): void {
  const root = document.documentElement;
  for (const [key, value] of Object.entries(theme.vars)) {
    root.style.setProperty(key, value);
  }
}
